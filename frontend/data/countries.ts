export const getShippingZone = (countryCode: string) => {
  for (const [zoneKey, zone] of Object.entries(shippingZones)) {
    if (zone.countries.includes(countryCode)) {
      return { key: zoneKey, ...zone };
    }
  }
  return { key: 'international', ...shippingZones.international };
};

export const getShippingCost = (countryCode: string, subtotal: number, express: boolean = false) => {
  const zone = getShippingZone(countryCode);
  
  if (subtotal >= zone.freeShippingThreshold) {
    return 0;
  }
  
  return express ? zone.expressRate : zone.standardRate;
};